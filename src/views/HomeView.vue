const walletIns = reactive<{ data: { web3Ins: any, web3Provider: any } }>({
    data: {
        web3Ins: {},
        web3Provider: {}
    }
})
const linkStatus = ref(false)
const user = reactive({
    data: {
        account: '',
        balance: ''
    }
})
const connect = async () => {
    if (user.data.account) {
        showLoadingToast({
            type: 'success',
            message: 'Link Success',
            forbidClick: true
        })
        return;
    }

    linkStatus.value = true
    const web3 = await CONNECT_WALLET()
    if (web3.web3Ins) {
        walletIns.data = web3

        web3.web3Provider.on('accountsChanged', async () => {
            user.data = {
                account: '',
                balance: ''
            }
            await connect()
        })

        user.data = await GET_WALLET_INFO()
    } else {
        showNotify({ type: 'warning', message: `Link Failed: ${web3.errmsg}` })
    }
    linkStatus.value = false
}

